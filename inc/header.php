<script>
    const submenuButtons = document.querySelectorAll('.ecl-menu__button-caret');
    const menu = document.querySelectorAll('.menu');
    const section = document.querySelectorAll('.ecl-menu__inner');
    const menuTitle = document.querySelectorAll('.ecl-menu__title');
    const backToMenu = document.querySelectorAll('.ecl-menu__back');

    submenuButtons.forEach((submenuButton) => {
        submenuButton.addEventListener('click', () => {
            //const menu = document.querySelectorAll('.menu');
            menu.forEach((item) => {
                item.classList.add('hidden');
            });
            section.forEach((item) => {
                item.classList.add('submenu-color');
            });
            menuTitle.forEach((item) => {
                item.classList.add('hidden');
            });
            backToMenu.forEach((item) => {
                item.classList.add('visible');
            });
            const submenu = submenuButton.nextElementSibling;
            submenu.classList.remove("hidden");
        });
    });


    const menuItems = document.querySelectorAll('.ecl-menu__item--has-children');

    menuItems.forEach((menuItem) => {
        menuItem.addEventListener('click', () => {
            menuItems.forEach((item) => {
                item.classList.remove('ecl-menu__item--expanded');
                item.classList.add('hidden');
            });
            menuItem.classList.add('ecl-menu__item--expanded');
            menuItem.classList.remove('hidden');
        });
    });


    const backButton = document.querySelector('.ecl-menu__back');

    backButton.addEventListener('click', () => {
        menu.forEach((item) => {
            item.classList.remove('hidden');
        });
        section.forEach((item) => {
            item.classList.remove('submenu-color');
        });
        menuTitle.forEach((item) => {
            item.classList.remove('hidden');
        });
        backToMenu.forEach((item) => {
            item.classList.remove('visible');
        });
        menuItems.forEach((item) => {
            item.classList.remove('ecl-menu__item--expanded');
            item.classList.remove('hidden');
        });
    });


</script>